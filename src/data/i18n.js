const slugify = (segment) => {
  return String(segment)
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[äàáâãå]/g, "ae")
    .replace(/[öòóôõ]/g, "oe")
    .replace(/[üùúû]/g, "ue")
    .replace(/[ß]/g, "ss")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
};

const trimSlashes = (value) => String(value).replace(/^\/+|\/+$/g, "");

export const translateHref = (href, locale) => {
  if (!href) return href;
  if (href.startsWith("http") || href === "#") return href;
  if (locale === "DE") return href;

  const path = trimSlashes(href);
  const segments = path.split("/").filter(Boolean);
  const translatedSegments = segments.map((segment) => {
    const mapped = slugMap[segment];
    if (mapped && mapped[locale]) return mapped[locale];
    return slugify(segment);
  });
  const localePrefix = localePaths[locale] || "";
  let result = `${localePrefix}/${translatedSegments.join("/")}/`;
  result = result.replace(/\/\/+/g, "/");
  if (!result.startsWith("/")) result = "/" + result;
  return result;
};

export const translate = (label, locale) => {
  return navLabelMap[label]?.[locale] || label;
};
